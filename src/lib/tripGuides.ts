/**
 * Tennessee State Parks trip-guide registry.
 *
 * Every TN state park has metadata here. Parks with written long-form
 * guides have `hasGuide: true` and link to the markdown in
 * content/trips/{slug}.md.
 *
 * Built incrementally — 62 parks total, guides added park-by-park.
 */

export type TripRegion = 'west' | 'middle' | 'east';

export type TripEcoregion =
  | 'mississippi-flyway'
  | 'river-bluff-lake'
  | 'western-highland-rim'
  | 'central-basin'
  | 'eastern-highland-rim'
  | 'cumberland-plateau'
  | 'ridge-and-valley'
  | 'southern-appalachian'
  | 'urban';

export type TripPriority = 1 | 2 | 3 | 4 | 5;

export interface TripGuide {
  slug: string;
  name: string;
  region: TripRegion;
  ecoregion: TripEcoregion;
  priority: TripPriority;
  county: string;
  acres: number;
  /** [lng, lat] */
  coords: [number, number];
  /** One-line habitat summary */
  habitat: string;
  /** 1–3 signature bird names for quick reference */
  signatureBirds: string[];
  /** Rough drive time from Memphis (e.g. "3 hr") */
  driveFromMemphis: string;
  /** If true, the park has a dedicated markdown trip guide */
  hasGuide: boolean;
  /** Short "why go here" tagline */
  tagline: string;
}


// ──────────────────────────────────────────────────────────────────────────────
// Query helpers

export function getTripBySlug(slug: string): TripGuide | undefined {
  return TN_STATE_PARKS.find((p) => p.slug === slug);
}

export function getTripsByRegion(region: TripRegion): TripGuide[] {
  return TN_STATE_PARKS.filter((p) => p.region === region);
}

export function getTripsByEcoregion(ecoregion: TripEcoregion): TripGuide[] {
  return TN_STATE_PARKS.filter((p) => p.ecoregion === ecoregion);
}

export function getTopPriorityTrips(minPriority: TripPriority = 4): TripGuide[] {
  return TN_STATE_PARKS.filter((p) => p.priority >= minPriority).sort(
    (a, b) => b.priority - a.priority || a.name.localeCompare(b.name),
  );
}

export function getTripsWithGuides(): TripGuide[] {
  return TN_STATE_PARKS.filter((p) => p.hasGuide);
}
